#!/usr/bin/env python3

#Create a set called cities (notice the {})
cities={'Ottawa','London','Paris','Beijing'}

#Print the set (not ordered)
print(cities)
print()

#Printing all of the set values using a for loop 
for city in cities:
	print(city)
print()

#Adding 2 values to the set
cities.add('Rome')
cities.add('Delhi')

#Printing all of the set values using a for loop 
for city in cities:
	print(city)
print()


